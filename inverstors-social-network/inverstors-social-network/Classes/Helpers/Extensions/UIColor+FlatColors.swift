import UIKit


extension UIColor {
    struct FlatColor {
        struct Green {
            static let strongGreen = UIColor(netHex: 0x6ABD0F)
        }
    
        struct Blue {
            static let strongCyan = UIColor(netHex: 0x10B6CB)
            static let vividCyanBlue = UIColor(netHex: 0x1A71E5)
        }
        
        struct Red {
            static let vividPinkRed = UIColor(netHex: 0xD0021B)
        }
        
        struct Gray {
            static let veryLight = UIColor(netHex: 0xE5E5E5)
        }
    }
}
